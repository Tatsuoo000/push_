#include "pushswap.h"

void	do_rb(s_stack **b)
{
	long	tmp;

	while ((*b)->prev->value != DUMMY)
	{
		*b = (*b)->next;
	}
	tmp = (*b)->value;
	(*b)->value = DUMMY;
	(*b)->prev->value = tmp;
}
