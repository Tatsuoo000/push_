#include "pushswap.h"

void	do_rra(s_stack **a)
{
	int	tmp;

	while ((*a)->next->value != 0)
	{
		*a = (*a)->next;
	}
	tmp = (*a)->value;
	(*a)->value = 0;
	(*a)->next->value = tmp;
}
